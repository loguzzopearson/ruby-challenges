fav_color = 'purple'

case fav_color
when 'red'
	puts "Red like fire!"
when 'orange' 
	puts "Orange like, well...an orange!"
when 'yellow'
	puts "Yellow daffodils are so pretty in the Spring!"
when 'green'
	puts "Have you been to the Emerald City in Oz?"
when 'blue'
	puts "Blue like the sky!"
when 'purple' 
	puts "Purple plums are the tastiest."
else 
	puts "Hmm, well I don't know what that color is!"
end


weather = 'sunny'

case weather
when 'rainy'
	puts "Put on your wellies and splash some puddles."
when 'foggy'
	puts "Throw on a parka and a headlamp."
when 'cloudy'
	puts "No need for sunnies, but wear your sunscreen anyway."
when 'snowstorm'	
	puts "Grab your skis baby & let's hit the slopes!"
when 'sunny'	
	puts "Dark days are over, let the good times roll!"
else
	puts "Funny weather, eh?"
end

